Param(
[Parameter(Mandatory=$true, ValueFromPipeline = $true)] $UserName,
$ComputerName
)

$Userinfo = Get-aduser $username -Properties SID

#Enter-PSSession -ComputerName ctxxa06 -Credential kcs\bwelmers

$UserExists =  'Microsoft.PowerShell.Core\Registry::HKEY_USERS\'+$userinfo.sid.value
$profilePath = $UserExists + '\software\microsoft\office\15.0\outlook\profiles'
if(!($ComputerName)){
    $ComputerName = @("XenAppServer01","XenAppServer02","XenAppServer03","CTXXA06")
}

foreach ($Server in $ComputerName){
    Write-Host "processing server " $server
    $UserOnServer = Invoke-Command -ComputerName $Server -ScriptBlock {test-path $args} -ArgumentList $UserExists

    If ($UserOnServer -eq $true){
        Write-host "User is on server"
        $AvailProfiles = Invoke-Command -ComputerName $Server -ScriptBlock {Get-ChildItem $args} -ArgumentList $Profilepath

        if ($AvailProfiles.name){

            if ($AvailProfiles.count -gt 0){
                foreach ($a in $AvailProfiles){
                    $deletekey = 'Microsoft.PowerShell.Core\Registry::' + $a.name
                    Invoke-Command -ComputerName $Server -ScriptBlock {remove-item $args -Recurse} -ArgumentList $deletekey
                    write-host "removed entry " $deletekey
                }

            }else{
                $deletekey = 'Microsoft.PowerShell.Core\Registry::' + $AvailProfiles.name
                Invoke-Command -ComputerName $Server -ScriptBlock {remove-item $args -Recurse} -ArgumentList $deletekey
                write-host "removed (single) entry " $deletekey
            }
        }
    $newProfile = $profilePath+'\NewProfile'
    Invoke-Command -ComputerName $Server -ScriptBlock {new-item $args } -ArgumentList $newProfile
    }
}

